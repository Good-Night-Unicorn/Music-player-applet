				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`gequxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='gequmingcheng'){
					this.ruleForm.gequmingcheng = obj[o];
					this.ro.gequmingcheng = true;
					continue;
					}
					if(o=='gequfenlei'){
					this.ruleForm.gequfenlei = obj[o];
					this.ro.gequfenlei = true;
					continue;
					}
					if(o=='zhuanjimingcheng'){
					this.ruleForm.zhuanjimingcheng = obj[o];
					this.ro.zhuanjimingcheng = true;
					continue;
					}
					if(o=='faxingfang'){
					this.ruleForm.faxingfang = obj[o];
					this.ro.faxingfang = true;
					continue;
					}
					if(o=='geshou'){
					this.ruleForm.geshou = obj[o];
					this.ro.geshou = true;
					continue;
					}
