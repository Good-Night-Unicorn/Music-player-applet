					this.ruleForm.yinpin = obj[o];
					this.ro.yinpin = true;
					continue;
					}
					if(o=='geci'){
					this.ruleForm.geci = obj[o];
					this.ro.geci = true;
					continue;
					}
					if(o=='fengmian'){
					this.ruleForm.fengmian = obj[o];
					this.ro.fengmian = true;
					continue;
					}
					if(o=='clicktime'){
					this.ruleForm.clicktime = obj[o];
					this.ro.clicktime = true;
					continue;
					}
					if(o=='clicknum'){
					this.ruleForm.clicknum = obj[o];
					this.ro.clicknum = true;
					continue;
					}
