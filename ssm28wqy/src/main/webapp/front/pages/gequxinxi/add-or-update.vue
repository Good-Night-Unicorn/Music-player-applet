				this.ruleForm.clicktime = val.result;
				this.$forceUpdate();
			},
												
															// 下拉变化
			gequfenleiChange(e) {
				this.gequfenleiIndex = e.target.value
				this.ruleForm.gequfenlei = this.gequfenleiOptions[this.gequfenleiIndex]
			},
																																																												
																																																																		yinpinTap () {
				let _this = this;
				this.$api.uploadMedia(function(res) {
					_this.ruleForm.yinpin = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
				});
			},
																					fengmianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.fengmian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																											
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																																																																																																																																								if((!this.ruleForm.gequmingcheng)){
