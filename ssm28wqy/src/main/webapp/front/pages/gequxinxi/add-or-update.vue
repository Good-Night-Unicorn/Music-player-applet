		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																																								
																		// 下拉框
			res = await this.$api.option(`gequfenlei`,`leixing`,{});
			this.gequfenleiOptions = res.data;
																																																															
			// 如果有登陆，获取登陆后保存的userid
