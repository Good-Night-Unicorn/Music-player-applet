                                   xingming : false,
                                   huiyuankahao : false,
                                   huiyuanleixing : false,
                                   huiyuandengji : false,
                                   banlishijian : false,
                                   daoqishijian : false,
                                   beizhu : false,
                                   tupian : false,
                                   userid : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																																		
												// 下2
			res = await this.$api.option(`yonghu`,`zhanghao`,{});
			this.zhanghaoOptions = res.data;
																																																															
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`huiyuanxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='zhanghao'){
					this.ruleForm.zhanghao = obj[o];
					this.ro.zhanghao = true;
					continue;
					}
					if(o=='xingming'){
					this.ruleForm.xingming = obj[o];
					this.ro.xingming = true;
					continue;
					}
					if(o=='huiyuankahao'){
					this.ruleForm.huiyuankahao = obj[o];
					this.ro.huiyuankahao = true;
					continue;
					}
					if(o=='huiyuanleixing'){
					this.ruleForm.huiyuanleixing = obj[o];
					this.ro.huiyuanleixing = true;
					continue;
					}
					if(o=='huiyuandengji'){
					this.ruleForm.huiyuandengji = obj[o];
					this.ro.huiyuandengji = true;
					continue;
					}
					if(o=='banlishijian'){
					this.ruleForm.banlishijian = obj[o];
					this.ro.banlishijian = true;
					continue;
					}
					if(o=='daoqishijian'){
					this.ruleForm.daoqishijian = obj[o];
					this.ro.daoqishijian = true;
					continue;
					}
					if(o=='beizhu'){
					this.ruleForm.beizhu = obj[o];
					this.ro.beizhu = true;
					continue;
					}
					if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					this.ro.tupian = true;
					continue;
					}
					if(o=='userid'){
					this.ruleForm.userid = obj[o];
					this.ro.userid = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
												// 下二随
			async zhanghaoChange (e) {
				this.zhanghaoIndex = e.target.value
				this.ruleForm.zhanghao = this.zhanghaoOptions[this.zhanghaoIndex]
				let res = await this.$api.follow(`yonghu`, `zhanghao`,{
					columnValue: this.ruleForm.zhanghao
				});
																				if(res.data.xingming){
					this.ruleForm.xingming = res.data.xingming
				}
																																																																											},
																																																												
			// 多级联动参数
																																																																		
																																							banlishijianChange(e) {
				this.ruleForm.banlishijian = e.target.value;
				this.$forceUpdate();
			},
												daoqishijianChange(e) {
				this.ruleForm.daoqishijian = e.target.value;
				this.$forceUpdate();
			},
																								
																																																															
																																																															
																																																																																	tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																		
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																																								if(this.ruleForm.huiyuandengji&&(!this.$validate.isIntNumer(this.ruleForm.huiyuandengji))){
					this.$utils.msg(`会员等级应输入整数`);
					return
				}
																																																																																																																																																																																																																								if(this.ruleForm.id){
					await this.$api.update(`huiyuanxinxi`, this.ruleForm);
				}else{
					await this.$api.add(`huiyuanxinxi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

